package Pertemuan14.Tugas1;

// Konstanta Global dengan static final
public class AppConfig {
    // Konstanta global menggunakan static final
    // static: dapat diakses tanpa membuat instance
    // final: nilai tidak dapat diubah setelah inisialisasi
    public static final String BASE_URL = "https://www.myapp.com/api";
    public static final String APP_VERSION = "1.0.0";
    public static final int DEFAULT_TIMEOUT = 5000; // dalam milidetik
}
